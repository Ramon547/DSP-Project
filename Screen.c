void SCREEN_SetCursor(uint8_t col, uint8_t page, I2C_HandleTypeDef *oled_i2c) {
    SCREEN_WriteCommand(0xB0 + page, oled_i2c);              // Set page
    SCREEN_WriteCommand(0x10 | (col >> 4), oled_i2c);        // Set high column
    SCREEN_WriteCommand(0x00 | (col & 0x0F), oled_i2c);      // Set low column

}

