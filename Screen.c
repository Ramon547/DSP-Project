void SCREEN_WriteCommand(uint8_t cmd, I2C_HandleTypeDef *oled_i2c) {
    uint8_t control = 0x00;
    HAL_I2C_Mem_Write(oled_i2c, SCREEN_I2C_ADDR, control, I2C_MEMADD_SIZE_8BIT, &cmd, 1, HAL_MAX_DELAY);
}

void SCREEN_WriteData(uint8_t *data, size_t size, I2C_HandleTypeDef *oled_i2c) {
    uint8_t control = 0x06;
    HAL_I2C_Mem_Write(oled_i2c, SCREEN_I2C_ADDR, control, I2C_MEMADD_SIZE_8BIT, data, size, HAL_MAX_DELAY);
}

